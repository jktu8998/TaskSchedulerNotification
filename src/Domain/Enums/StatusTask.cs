namespace Domain.Enums;

/// <summary>
/// Статусы жизненного цикла задания. 
/// Определяют, в каком состоянии находится задание в данный момент.
/// </summary>
public enum StatusTask
{
    /// <summary>Задание создано, но ещё не запланировано.</summary>
    Created = 0,

    /// <summary>Задание запланировано, ожидает наступления времени выполнения.</summary>
    Scheduled = 1,

    /// <summary>Время выполнения наступило, задание помещено в очередь на исполнение.</summary>
    Queued = 2,

    /// <summary>Задание выполняется в данный момент (отправлен HTTP-запрос).</summary>
    Executing = 3,

    /// <summary>Задание успешно выполнено.</summary>
    Completed = 4,

    /// <summary>Выполнение завершилось ошибкой, но есть повторные попытки.</summary>
    Failed = 5,

    /// <summary>Все попытки исчерпаны, задание перемещено в Dead Letter Queue.</summary>
    Dead = 6,

    /// <summary>Задание приостановлено пользователем (для периодических заданий).</summary>
    Paused = 7,

    /// <summary>Задание отменено и больше не будет выполняться.</summary>
    Cancelled = 8
}